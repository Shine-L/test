#鸡翁一值钱五，鸡母一值钱三，鸡雏三值钱一，百钱买百鸡，问鸡翁，鸡母，鸡雏各几只
#用穷举法，鸡翁一只5元，100块可以买20只，鸡母一只3元，100块可以买33只，
# 所以设鸡翁x只，鸡母y只，鸡雏就是100-x-y只
#x的范围是[1,20),y的范围是[1,33)
#满足5*x+3*y+(100-x-y)/3=100

for x in range(1,20):
    for y in range(1,33):
        z=100-x-y
        if 5*x+3*y+z/3==100:
            print("鸡翁{}只，鸡母{}只，鸡雏{}只".format(x,y,z))